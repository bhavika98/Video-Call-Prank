@Composable
fun ThemeSwitcherTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> DarkColors
        else -> LightColors
    }
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            (view.context as Activity).window.statusBarColor = colorScheme.primary.toArgb()
            ViewCompat.getWindowInsetsController(view)?.isAppearanceLightStatusBars = darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}

const val customBackground = "customBackground"
const val customTextTitle = "customTextTitle"
const val customTextSubtitle = "customTextSubtitle"
const val customCardMain = "customCardMain"
const val customPanel = "customPanel"
const val customBottomIcon = "customBottomIcon"
const val customButton = "customButton"
const val customTextTitleOpposite = "customTextTitleOpposite"

fun getTheme(darkTheme: Boolean, id: String) = when (id) {
    customBackground -> {
        if (darkTheme) custom_dark_background else custom_light_background
    }

    customTextTitle -> {
        if (darkTheme) custom_dark_text_title_main else custom_light_text_title_main
    }

    customTextTitleOpposite -> {
        if (!darkTheme) custom_dark_text_title_main else custom_light_text_title_main
    }

    customTextSubtitle -> {
        if (darkTheme) custom_dark_text_sub_title_main else custom_light_text_sub_title_main
    }

    customCardMain -> {
        if (darkTheme) custom_dark_card_main else custom_light_card_main
    }

    customPanel -> {
        if (darkTheme) custom_dark_panel else custom_light_panel
    }

    customBottomIcon -> {
        if (darkTheme) custom_dark_bottom_icon else custom_light_bottom_icon
    }

    customButton -> {
        if (darkTheme) custom_dark_button else custom_light_button
    }

    else -> {
        md_theme_dark_inversePrimary
    }
}

